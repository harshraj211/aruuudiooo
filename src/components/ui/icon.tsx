interface IconProps extends LucideProps {
  name: IconName;
}

export const Icon = ({ name, ...props }: IconProps) => {
  const LucideIcon = iconMap[name];
  if (!LucideIcon) {
    return <Circle {...props} />; // Return a default icon or null
  }
  return <LucideIcon {...props} />;
};
